const fs = require('fs');
const path = require('path');

const projects = [
  '/home/biibon/Crawler-AI-video-douyin',
  '/home/biibon/auto-edit-video-reup-tool',
  '/home/biibon/game-store'
];

for (const projectDir of projects) {
  const analysisPath = path.join(projectDir, '.codeatlas', 'analysis.json');
  if (!fs.existsSync(analysisPath)) {
    console.log(projectDir + ': NO ANALYSIS - SKIP');
    continue;
  }

  const memoryDir = path.join(projectDir, '.agents', 'memory');
  fs.mkdirSync(memoryDir, { recursive: true });

  const analysis = JSON.parse(fs.readFileSync(analysisPath, 'utf8'));
  const nodes = analysis.graph.nodes;
  const links = analysis.graph.links;
  const projectName = path.basename(projectDir);
  const nodeMap = new Map(nodes.map(n => [n.id, n]));

  // === 1. system-map.md ===
  const modules = nodes.filter(n => n.type === 'module' && n.filePath);
  const classes = nodes.filter(n => n.type === 'class');
  const functions = nodes.filter(n => n.type === 'function');

  // Module-to-module import links
  const moduleLinks = links.filter(l => {
    const src = nodeMap.get(l.source);
    const tgt = nodeMap.get(l.target);
    return src && tgt && src.type === 'module' && tgt.type === 'module' && src.filePath && tgt.filePath && l.type === 'import';
  });

  // Deduplicate
  const mlSet = new Set();
  const dedupModuleLinks = moduleLinks.filter(l => {
    const key = l.source + '|' + l.target;
    if (mlSet.has(key)) return false;
    mlSet.add(key);
    return true;
  });

  // Connection count per module
  const moduleConnections = new Map();
  for (const link of links) {
    if (link.type === 'import') {
      moduleConnections.set(link.source, (moduleConnections.get(link.source) || 0) + 1);
      moduleConnections.set(link.target, (moduleConnections.get(link.target) || 0) + 1);
    }
  }

  // Key modules sorted by connections
  const keyModules = modules
    .map(m => ({ name: m.label, path: m.filePath, connections: moduleConnections.get(m.id) || 0 }))
    .sort((a, b) => b.connections - a.connections)
    .slice(0, 20);

  // Build Mermaid
  const topModules = modules.slice(0, 50);
  const topModuleIds = new Set(topModules.map(m => m.id));
  const topModuleLinks = dedupModuleLinks.filter(l => topModuleIds.has(l.source) && topModuleIds.has(l.target));

  let mermaid = '```mermaid\ngraph TD\n';
  let nodeCounter = 0;
  const mermaidIdMap = new Map();
  for (const mod of topModules) {
    const mid = 'm' + nodeCounter++;
    mermaidIdMap.set(mod.id, mid);
    mermaid += '    ' + mid + '["📄 ' + mod.label + '"]\n';
  }
  for (const link of topModuleLinks) {
    const s = mermaidIdMap.get(link.source);
    const t = mermaidIdMap.get(link.target);
    if (s && t) mermaid += '    ' + s + ' -->|imports| ' + t + '\n';
  }
  mermaid += '```';

  const systemMap = [
    '# System Map — ' + projectName,
    '> Auto-generated by CodeAtlas on ' + new Date().toISOString(),
    '> **DO NOT EDIT MANUALLY** — regenerated by `sync_system_memory`',
    '',
    '## Overview',
    '- **Modules**: ' + modules.length,
    '- **Classes**: ' + classes.length,
    '- **Functions**: ' + functions.length,
    '- **Total links**: ' + links.length,
    '',
    '## Module Dependency Graph',
    mermaid,
    '',
    '## Key Modules (by connection count)',
    ...keyModules.map((m, i) => (i + 1) + '. **' + m.name + '** (' + m.connections + ' connections) — `' + path.relative(projectDir, m.path || '') + '`'),
    ''
  ].join('\n');

  fs.writeFileSync(path.join(memoryDir, 'system-map.md'), systemMap);

  // === 2. modules.json ===
  const modulesJson = modules.map(m => {
    const contained = links
      .filter(l => l.source === m.id && l.type === 'contains')
      .map(l => {
        const target = nodeMap.get(l.target);
        return target ? { name: target.label, type: target.type } : null;
      })
      .filter(Boolean);

    const imports = links
      .filter(l => l.source === m.id && l.type === 'import')
      .map(l => {
        const target = nodeMap.get(l.target);
        return target ? target.label : l.target;
      });

    return {
      name: m.label,
      path: path.relative(projectDir, m.filePath || ''),
      contains: contained,
      imports: imports,
      connections: moduleConnections.get(m.id) || 0
    };
  });

  fs.writeFileSync(path.join(memoryDir, 'modules.json'), JSON.stringify(modulesJson, null, 2));

  // === 3. feature-flows.json ===
  // Group by directory
  const featureMap = new Map();
  for (const mod of modules) {
    if (!mod.filePath) continue;
    const rel = path.relative(projectDir, mod.filePath);
    const dir = path.dirname(rel).split(path.sep)[0] || '.';
    if (!featureMap.has(dir)) featureMap.set(dir, []);
    featureMap.get(dir).push(rel);
  }

  const featureFlows = {};
  for (const [dir, files] of featureMap) {
    // Entry points: files with most outgoing imports to other internal modules
    const entryPoints = files
      .map(f => {
        const modNode = modules.find(m => m.filePath && path.relative(projectDir, m.filePath) === f);
        if (!modNode) return { file: f, outgoing: 0 };
        const outgoing = links.filter(l => l.source === modNode.id && l.type === 'import').length;
        return { file: f, outgoing };
      })
      .sort((a, b) => b.outgoing - a.outgoing)
      .slice(0, 3)
      .map(e => e.file);

    featureFlows[dir] = { files, entryPoints };
  }

  // Also add keyword-based feature detection
  const keywordMap = {};
  for (const n of nodes) {
    if (!n.filePath || !n.label) continue;
    const parts = n.label.toLowerCase().split(/[_.\-\/]/);
    for (const p of parts) {
      if (p.length > 3 && !['init', 'self', 'none', 'true', 'false', 'main', 'test', 'utils', 'helper', 'base', 'type', 'node', 'path', 'file', 'data', 'list', 'item'].includes(p)) {
        if (!keywordMap[p]) keywordMap[p] = new Set();
        keywordMap[p].add(path.relative(projectDir, n.filePath));
      }
    }
  }

  // Only keep keywords appearing in 2-15 files (meaningful features)
  const keywordFeatures = {};
  for (const [k, v] of Object.entries(keywordMap)) {
    if (v.size >= 2 && v.size <= 15) {
      keywordFeatures[k] = [...v];
    }
  }

  featureFlows['_keywords'] = keywordFeatures;

  fs.writeFileSync(path.join(memoryDir, 'feature-flows.json'), JSON.stringify(featureFlows, null, 2));

  // === 4. business-rules.json ===
  const brPath = path.join(memoryDir, 'business-rules.json');
  if (!fs.existsSync(brPath) || fs.readFileSync(brPath, 'utf8').trim() === '[]') {
    fs.writeFileSync(brPath, JSON.stringify([], null, 2));
  }

  // === 5. change-log.json ===
  const clPath = path.join(memoryDir, 'change-log.json');
  let changeLog = [];
  if (fs.existsSync(clPath)) {
    try {
      const parsedCL = JSON.parse(fs.readFileSync(clPath, 'utf8'));
      changeLog = Array.isArray(parsedCL) ? parsedCL : [];
    } catch {}
  }
  if (changeLog.length === 0 || changeLog[0].description === 'Initial memory sync') {
    changeLog = [{ timestamp: new Date().toISOString(), description: 'Memory regenerated with full feature detection' }];
  }
  fs.writeFileSync(clPath, JSON.stringify(changeLog, null, 2));

  // === 6. conventions.md ===
  const langs = new Map();
  modules.forEach(m => {
    if (!m.filePath) return;
    const ext = path.extname(m.filePath);
    langs.set(ext, (langs.get(ext) || 0) + 1);
  });
  const topLangs = Array.from(langs.entries()).sort((a, b) => b[1] - a[1]).slice(0, 5);
  const dirs = Array.from(featureMap.keys()).sort();

  const hasControllers = modules.some(m => m.filePath && (m.filePath.includes('controller') || m.filePath.includes('Controller')));
  const hasModels = modules.some(m => m.filePath && (m.filePath.includes('model') || m.filePath.includes('Model')));
  const hasServices = modules.some(m => m.filePath && (m.filePath.includes('service') || m.filePath.includes('Service')));
  const hasWorkers = modules.some(m => m.filePath && (m.filePath.includes('worker') || m.filePath.includes('Worker')));

  const conventions = [
    '# Conventions — ' + projectName,
    '> Auto-generated by CodeAtlas on ' + new Date().toISOString(),
    '> **DO NOT EDIT MANUALLY**',
    '',
    '## Languages',
    ...topLangs.map(([ext, count]) => '- `' + ext + '`: ' + count + ' files'),
    '',
    '## Project Structure',
    ...dirs.map(d => '- `' + d + '/` — ' + (featureMap.get(d) || []).length + ' files'),
    '',
    '## Architecture Patterns',
    hasControllers ? '- ✅ MVC Pattern (Controllers detected)' : '',
    hasModels ? '- ✅ Models layer detected' : '',
    hasServices ? '- ✅ Service layer detected' : '',
    hasWorkers ? '- ✅ Worker/Queue pattern detected' : '',
    ''
  ].filter(Boolean).join('\n');

  fs.writeFileSync(path.join(memoryDir, 'conventions.md'), conventions);

  const dirCount = Object.keys(featureFlows).filter(k => k !== '_keywords').length;
  const kwCount = Object.keys(keywordFeatures).length;
  console.log('✅ ' + projectName + ': .agents/memory/ synced (' + dirCount + ' dirs, ' + kwCount + ' keyword features, ' + keyModules.length + ' key modules)');
}
